# String Slicing / selecting sub sequences
lang = "Rinku"
# Syntax- [start argument : stop argument - 1]
# Syntax- [start argument : stop argument - 1 : step argument]
print(lang[1:3])
print(lang[:]) # Print all string
print(lang[1:]) # Print start to go end 
print(lang[:3]) # Print start to end 3rd position

