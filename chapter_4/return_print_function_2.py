# Return vs Print in function- not neccesary to write return in function, you can also write print in function

# Use return in function
# Make sum of three no with function
def add_three(a,b,c):
    return a+b+c
# call function to get output
print(add_three(5,6,7))

# Now call Print in Function
def add_three(a,b,c):
    print(a+b+c)

