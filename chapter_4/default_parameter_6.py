# Default Parameter - None - Special Value

# You can assign default at last parameter or make default to all parameter or only last parameter to make default.

def user_info(first_name,last_name,age):
    print(f"First name is {first_name}")
    print(f"Last name is {last_name}")
    print(f"Age is {age}")


user_info('Rinku','Vishwakarma',30)