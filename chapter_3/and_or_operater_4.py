# AND OR Operater Statement
name = "rinku"
age = 15
# When All condition are true then return true
if name=="rinku" and age==15:
    print("condition True")
else:
    print("Condition False")

# When only one condition are true then return true
if name=="rinku" or age==16:
    print("condition True")
else:
    print("Condition False")