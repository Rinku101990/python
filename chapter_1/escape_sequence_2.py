# Escape Sequences list
# \"\" - Double Quotes
# \'\' - Single Quotes
# \\ - baclslash(Double backslash means single backslash)
# \n - new line
# \t - tab
# \b - backspace
print("Hello \"Rinku\" World")
print('Hello \'Rinku\' World')
print("Hello Rinku Vishwakarma. \nHow are you.")
print("Hello Rinku\tVishwakarma.\nHow are you.")
print("Hello Rinku\tVishwakarma.\nHow are you.")
print("hell\blo")